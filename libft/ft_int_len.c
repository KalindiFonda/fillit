#include <libft.h>

int		ft_int_len(int n)
{
	int		len;

	len = 1;
	if (n == -2147483648)
		return (11);
	if (n < 0)
	{
		n *= -1;
		len++;
	}
	while ((n = n / 10))
		len++;
	return (len);
}
