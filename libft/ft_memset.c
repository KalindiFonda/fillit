#include <libft.h>

void	*ft_memset(void *s, int c, size_t n)
{
	int		i;
	char	*ps;

	i = 0;
	ps = (char *)s;
	while (n > 0)
	{
		ps[i] = (unsigned char)c;
		i++;
		n--;
	}
	return (s);
}
