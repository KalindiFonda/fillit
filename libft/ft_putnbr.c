#include <libft.h>

void	ft_putnbr(int n)
{
	char	c;

	if (n == -2147483648)
	{
		ft_putnbr(-214748364);
		write(1, "8", 1);
		return ;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	if (n / 10 > 0)
		ft_putnbr(n / 10);
	c = n % 10 + '0';
	write(1, &c, 1);
}
