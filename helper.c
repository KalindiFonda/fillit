#include "fillit.h"

void	ft_printcords(char tetri[5][2])
{
	int i;

	i = 0;
	while (i < 4)
	{
		printf("#%d\n", i);
		printf("Tetri X:%d\n", tetri[i][0]);
		printf("Tetri Y:%d\n", tetri[i][1]);
		i++;
	}
}
